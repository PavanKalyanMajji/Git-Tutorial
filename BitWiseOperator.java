package operators;

public class BitWiseOperator {
	public static void main(String[] args) {
		System.out.println("The value of After BitwiseAnd: "+(5&7));
		System.out.println("The value of After BitwiseOr: "+(5|7));
		System.out.println("The value of After BitWiseXOR: "+(5^7));
		System.out.println("The value of After Bitwise Complement: "+ ~(5));
	}
}
